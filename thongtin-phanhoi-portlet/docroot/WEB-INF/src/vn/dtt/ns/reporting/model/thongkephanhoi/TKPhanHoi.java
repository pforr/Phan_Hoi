package vn.dtt.ns.reporting.model.thongkephanhoi;

import java.util.List;

public class TKPhanHoi {
	private String tenTinh;
	private String thangNam;
	private List<TKPhanHoiDanhSach> lstPhanHoiDanhSach;
	
	public String getTenTinh() {
		return tenTinh;
	}

	public void setTenTinh(String tenTinh) {
		this.tenTinh = tenTinh;
	}

	public String getThangNam() {
		return thangNam;
	}

	public void setThangNam(String thangNam) {
		this.thangNam = thangNam;
	}

	public List<TKPhanHoiDanhSach> getLstPhanHoiDanhSach() {
		return lstPhanHoiDanhSach;
	}

	public void setLstPhanHoiDanhSach(List<TKPhanHoiDanhSach> lstPhanHoiDanhSach) {
		this.lstPhanHoiDanhSach = lstPhanHoiDanhSach;
	}

	public class TKPhanHoiDanhSach{
		private String chuDe;
		private int soPhanHoi;
		private int soPhanHoiDaTaoGiaiTrinh;
		private int soPhanHoiChuaTaoGiaiTrinh;
		public String getChuDe() {
			return chuDe;
		}
		public void setChuDe(String chuDe) {
			this.chuDe = chuDe;
		}
		public int getSoPhanHoi() {
			return soPhanHoi;
		}
		public void setSoPhanHoi(int soPhanHoi) {
			this.soPhanHoi = soPhanHoi;
		}
		public int getSoPhanHoiDaTaoGiaiTrinh() {
			return soPhanHoiDaTaoGiaiTrinh;
		}
		public void setSoPhanHoiDaTaoGiaiTrinh(int soPhanHoiDaTaoGiaiTrinh) {
			this.soPhanHoiDaTaoGiaiTrinh = soPhanHoiDaTaoGiaiTrinh;
		}
		public int getSoPhanHoiChuaTaoGiaiTrinh() {
			return soPhanHoiChuaTaoGiaiTrinh;
		}
		public void setSoPhanHoiChuaTaoGiaiTrinh(int soPhanHoiChuaTaoGiaiTrinh) {
			this.soPhanHoiChuaTaoGiaiTrinh = soPhanHoiChuaTaoGiaiTrinh;
		}
		
		
	}
}
